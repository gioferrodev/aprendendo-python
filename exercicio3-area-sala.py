# Escreva um algoritmo que peça ao usuário para entrar com a largura e comprimento
# de uma sala. Uma vez que os valores são lidos, mostre na tela a área da sala.

largura = float(input("digite a largura da sala: "))
altura = float(input("digite a altura da sala: "))

area = altura * largura

print(f"A sala tem {area} de área.")
